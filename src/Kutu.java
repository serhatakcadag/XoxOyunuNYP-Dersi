public class Kutu {
    private String deger;
    private boolean oynandi;

    public Kutu() {
        this.oynandi = false;
        this.deger = "";
    }

    public String getDeger() {
        return deger;
    }

    public void setDeger(String deger) {
        this.deger = deger;
    }

    public boolean isOynandi() {
        return oynandi;
    }

    public void setOynandi(boolean oynandi) {
        this.oynandi = oynandi;
    }
}
